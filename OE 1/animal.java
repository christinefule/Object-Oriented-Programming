public class animal {
    private String breed, color;

    // constructor of OBJECT VARIABLES
    public animal(String breed, String color) {
        this.breed = breed;
        this.color = color;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public String getColor() {
        return color;
    }

    public void setDog(String name, String breed, double weight, String color) {
        name = name;
        weight = weight;
    }

    public void setCat(String name, String breed, double weight, String color) {
        name = name;
        weight = weight;
    }

    public void setFish(String name, String breed, double weight, String color) {
        name = name;
        weight = weight;
    }

    public void setBird(String name, String breed, double weight, String color) {
        name = name;
        weight = weight;
    }
}